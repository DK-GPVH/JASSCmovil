class Propiedad {
  String id;
  String manzana;
  String lote;
  String zona;
  String nrodesuministro;
  String clienteid;
  String categoriaid;
  String estado;
  String fechains;
  String fechaad;

  Propiedad(
      this.id,
      this.manzana,
      this.lote,
      this.zona,
      this.nrodesuministro,
      this.clienteid,
      this.categoriaid,
      this.estado,
      this.fechains,
      this.fechaad) {
    this.id = id;
    this.manzana = manzana;
    this.lote = lote;
    this.zona = zona;
    this.nrodesuministro = nrodesuministro;
    this.clienteid = clienteid;
    this.categoriaid = categoriaid;
    this.estado = estado;
    this.fechains = fechains;
    this.fechaad = fechaad;
  }
}
